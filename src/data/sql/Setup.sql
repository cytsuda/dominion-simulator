--DECK TABLE
CREATE TABLE IF NOT EXISTS "Deck"(
  "ID" INTEGER PRIMARY KEY AUTOINCREMENT,
  "NAME" TEXT,
  "CREATOR" TEXT
);

--UNITY TYPE TABLE
CREATE TABLE IF NOT EXISTS "Unity"(
  "NAME" TEXT,
  "ELEMENT" TEXT,
  "TAG" TEXT,
  "ATK" TEXT,
  "HP" TEXT,
  "RANGE" TEXT,
  "EFFECT" TEXT,
  "North" TEXT,
  "North-Right" TEXT,
  "Right" TEXT,
  "South-Right" TEXT,
  "South" TEXT,
  "South-Left" TEXT,
  "Left" TEXT,
  "North-Left" TEXT,
  "LAYOUT" TEXT,
  "ART" TEXT,
  "NUM" TEXT PRIMARY KEY,
  "STAR" TEXT
);

--DECK + UNITY CARD RELATION TABLE
CREATE TABLE IF NOT EXISTS "DeckUnityCardRelation"(
    "DECK_ID" INTEGER NOT NULL,
    "UNITY_ID" INTEGER NOT NULL,
    "CARD_QUANTITY" INTEGER DEFAULT 1,
    PRIMARY KEY ("DECK_ID", "UNITY_ID"),
    FOREIGN KEY ("DECK_ID") REFERENCES "Deck"("ID"),
    FOREIGN KEY ("UNITY_ID") REFERENCES "Unity"("NUM") ON DELETE CASCADE
)
